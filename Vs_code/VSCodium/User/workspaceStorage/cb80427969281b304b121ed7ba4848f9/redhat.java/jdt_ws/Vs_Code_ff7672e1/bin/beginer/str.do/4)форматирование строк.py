#--------------------------------------------------------------f-строка-------------------------------------------------------------------

name = 'Андрей'
my_str = f'Привет {name}!'
print(my_str)

#---------------------------------------------------------------format--------------------------------------------------------------------

name = 'Анна'
my_str = 'Привет {}!'.format(name)
print(my_str)

#---------------------------------------------------------------%старый%------------------------------------------------------------------

name = 'Алекс'
my_str = 'Привет %s!'
print(my_str % name)



